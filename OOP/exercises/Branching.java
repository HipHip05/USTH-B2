public class Branching {
    public static void main(String[] args) {
        Integer a = 1;
        Integer b = 5;
        if (a < b) {
            System.out.print("Number A bigger than number B");
        } else if (a == b) {
            System.out.print("Number A equal to number B");
        } else {
            System.out.print("Number A less than number B");
        }
    }
}
